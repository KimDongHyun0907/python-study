words=['computer','bed','banana','pencil']
words.sort()
print(words) # ['banana', 'bed', 'computer', 'pencil']

words=['computer','bed','banana','pencil']
words.sort(reverse=True)
print(words) # ['pencil', 'computer', 'bed', 'banana']

words=['computer','bed','banana','pencil']
words=sorted(words)
print(words) # ['banana', 'bed', 'computer', 'pencil']

words=['computer','bed','banana','pencil']
words=sorted(words,reverse=True)
print(words) # ['pencil', 'computer', 'bed', 'banana']
